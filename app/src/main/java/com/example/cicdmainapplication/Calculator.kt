package com.example.cicdmainapplication

class Calculator {

    fun addition(x: Int, y: Int): Int {
        return x + y
    }

    fun subtraction(x: Int, y: Int): Int {
        return x - y
    }

    fun division(x: Int, y: Int): Int {
        return x / y
    }

    fun multiply(x: Int, y: Int): Int {
        return x * y
    }

    fun modulus(x: Int, y: Int): Int {
        return x % y
    }
}
